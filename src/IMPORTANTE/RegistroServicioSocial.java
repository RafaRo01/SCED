    public void addplaceholder(JTextField textField){
        Font font= textField.getFont();
        font = font.deriveFont(Font.ITALIC);
        textField.setFont(font);
        textField.setForeground(Color.gray);
       
    }
    public void deleteplaceholder(JTextField textField){
        Font font= textField.getFont();
        font = font.deriveFont(Font.PLAIN|Font.BOLD);
        textField.setFont(font);
        textField.setForeground(Color.black);
    }
    
    public void validar(){
        String ncon= txtNumControl.getText();
        String usu = txtUsuario.getText();
        String pass = String.valueOf(txtPass.getPassword());
        String nom= txtNombre.getText();
        String rol= cbxRol.getSelectedItem().toString();
        
        
        if(!"".equals(ncon) || !"".equals(pass) || !"".equals(nom)|| !"".equals(usu) ){
          lg.setNombre(nom);
          lg.setUsuario(usu);
          lg.setNumControl(ncon);
          lg.setClave(pass);
          lg.setPerfil(rol);
          
          
          login.RegistrarUsuarioServicioSocial(lg);
                  Login iniciar= new Login();
             iniciar.setVisible(true);
             dispose();
        }
    }
    
    
     
      public void validarServicioSocial(){
        String usu = txtUsuario.getText();
        String nom= txtNombre.getText();
        String numCont = txtNumControl.getText();
        String carrera = txtCarrera.getText();
        String turno = txtTurno.getText();
        String escuela = txtEscuela.getText();
        String pass = String.valueOf(txtPass.getPassword());
        String rol= cbxRol.getSelectedItem().toString();
        
        
        
        if(!"".equals(usu)|| !"".equals(pass) || !"".equals(nom)|| !"".equals(carrera)||!"".equals(numCont) ||!"".equals(turno) ||!"".equals(escuela) ){
          lg.setUsuario(usu);
          lg.setNombre(nom);
          lg.setNumControl(numCont);
          lg.setCarrera(carrera);
          lg.setTurno(turno);
          lg.setEscuela(escuela);
          lg.setClave(pass);
          lg.setPerfil(rol);
          
          
          login.RegistrarUsuarioServicioSocial(lg);
             Login iniciar= new Login();
             iniciar.setVisible(true);
             dispose();
        }
    }
      
    
    
    


  

