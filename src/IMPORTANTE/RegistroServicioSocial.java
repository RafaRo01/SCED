    private void cbxRolActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbxRolActionPerformed
      String op;
        op = cbxRol.getSelectedItem().toString();
        switch (op) {
                case "Jefe":
                    RegistroJefe je= new RegistroJefe();
        je.setVisible(true);
        dispose();
                    break;
                    
                case "Profesor":
                    RegistroProfesor pro= new RegistroProfesor();
        pro.setVisible(true);
        dispose();
                    break;
                    
                case "Servicio Social":
                    RegistroServicioSocial ss= new RegistroServicioSocial();
        ss.setVisible(true);
        dispose();
                    break;
                    
                case "Invitado":
                    RegistroInvitado inv= new RegistroInvitado();
        inv.setVisible(true);
        dispose();
                    break;
                
                    
        }
    }//GEN-LAST:event_cbxRolActionPerformed

