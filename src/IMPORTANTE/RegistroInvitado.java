    public void addplaceholder(JTextField textField){
        Font font= textField.getFont();
        font = font.deriveFont(Font.ITALIC);
        textField.setFont(font);
        textField.setForeground(Color.gray);
       
    }
    public void deleteplaceholder(JTextField textField){
        Font font= textField.getFont();
        font = font.deriveFont(Font.PLAIN|Font.BOLD);
        textField.setFont(font);
        textField.setForeground(Color.black);
    }
    
  
       public void validarInvitado(){
        String nom= txtNombre.getText();
        String puesto = txtPuesto.getText();
        String usu = txtUsuario.getText();
        String institucion = txtInstitucion.getText();
        String pass = String.valueOf(txtPass.getPassword());
        String rol= cbxRol.getSelectedItem().toString();
        
        
        if( !"".equals(pass) || !"".equals(nom) ||!"".equals(puesto) ||!"".equals(institucion)|| !"".equals(usu)){
          lg.setNombre(nom);
          lg.setUsuario(usu);
          lg.setPuesto(puesto);
          lg.setInstitucion(institucion);
          lg.setClave(pass);
          lg.setPerfil(rol);
          
          
          
          
          login.RegistrarUsuarioInvitado(lg);
             Login iniciar= new Login();
             iniciar.setVisible(true);
             dispose();
        }
    }
    
    


  

