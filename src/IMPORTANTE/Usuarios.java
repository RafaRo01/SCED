    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
         String msg = null;
        if (txtIdUsuarios.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Selecciona un registro de la tabla para eliminar", "Informacion", JOptionPane.ERROR_MESSAGE);
        } else {
            UsuariosDto dts = new UsuariosDto();
            UsuariosDao fun = new UsuariosDao();

            dts.setIdUsuarios(Integer.parseInt(txtIdUsuarios.getText()));

            msg = fun.eliminarUsuarios(dts);
            JOptionPane.showMessageDialog(rootPane, msg, "Usuario Eliminado", JOptionPane.INFORMATION_MESSAGE);
        }
        mostrarBuscar("");
       
       limpiar();
    }//GEN-LAST:event_btnEliminarActionPerformed

