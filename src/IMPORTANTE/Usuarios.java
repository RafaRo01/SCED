    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        
        String msg = null;
        if (txtNombre.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Nombre obligatorio", "Validacion", JOptionPane.WARNING_MESSAGE);
            txtNombre.requestFocusInWindow();
            return;
        }
        if (txtUsuario.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Usuario obligatorio", "Validacion", JOptionPane.WARNING_MESSAGE);
            txtUsuario.requestFocusInWindow();
            return;
        }
      
        String clave = new String(txtClave.getPassword());
        String confclave = new String(txtClave.getPassword());
        if (clave.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Campo clave obligatorio", "Validacion", JOptionPane.WARNING_MESSAGE);
            txtClave.requestFocusInWindow();
            return;
        }
        
     
        if (txtPerfil.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Campo perfil obligatorio", "Validacion", JOptionPane.WARNING_MESSAGE);
            txtPerfil.requestFocusInWindow();
            return;
        }
        if (txtIdUsuarios.getText().equals("")) {
            UsuariosDto usuDto = new UsuariosDto();
            UsuariosDao usuDao = new UsuariosDao(); 
            
            //String perfil = String.valueOf(cmbPerfil.getSelectedItem());
            usuDto.setNombre(txtNombre.getText() );
            usuDto.setUsuario(txtUsuario.getText());
            usuDto.setNumFicha(txtnFicha.getText());
            usuDto.setNumControl(txtnControl.getText());
            usuDto.setCarrera(txtCarrera.getText());
            usuDto.setTurno(txtTurno.getText());
            usuDto.setPuesto(txtPuesto.getText());
            usuDto.setInstitucion(txtInstitucion.getText());
            usuDto.setEscuela(txtEscuela.getText());
            usuDto.setClave(txtClave.getText());
            usuDto.setPerfil(txtPerfil.getText());
            
            
            msg = usuDao.insertarUsuarios(usuDto);
            JOptionPane.showMessageDialog(rootPane, msg, "Usuario Agregado", JOptionPane.INFORMATION_MESSAGE);
        } else {
            UsuariosDto usDto = new UsuariosDto();
            UsuariosDao usDao = new UsuariosDao();
           
        //    String perfil = String.valueOf(cmbPerfil.getSelectedItem());

            usDto.setIdUsuarios(Integer.parseInt(txtIdUsuarios.getText()));
            usDto.setNombre(txtNombre.getText());
            usDto.setUsuario(txtUsuario.getText());
            usDto.setNumFicha(txtnFicha.getText());
            usDto.setNumControl(txtnControl.getText());
            usDto.setCarrera(txtCarrera.getText());
            usDto.setTurno(txtTurno.getText());
            usDto.setPuesto(txtPuesto.getText());
            usDto.setInstitucion(txtInstitucion.getText());
            usDto.setEscuela(txtEscuela.getText());
            usDto.setClave(txtClave.getText());
            usDto.setPerfil(txtPerfil.getText());
            
          
            msg = usDao.editarUsuarios(usDto);
            JOptionPane.showMessageDialog(rootPane, msg, "Usuario Modificado", JOptionPane.INFORMATION_MESSAGE);
        }
       
       
        
        habilitar(true);
        mostrarBuscar("");
    }//GEN-LAST:event_btnGuardarActionPerformed

